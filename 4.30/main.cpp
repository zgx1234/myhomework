#include <iostream>

using namespace std;

int main()
{
    double r=0;
    double l=0;
    double s=0;
    double S=0;
    double pi=3.14159;
    cout<<"Enter the radius = ";
    cin >>r;
    l=2*r;
    s=2*pi*r;
    S=pi*r*r;
    cout<<"l="<<l;
    cout<<"\ns="<<s;
    cout<<"\nS="<<S;
    return 0;


}
